// The most basic ones are
// int - Any whole number, e.g. 0, 5, 502781, -3
// float - Any decimal number, e.g. 1.5, 2, -0.11111111
// bool - True or False, this is essentully a switch, on or off

// Assigning

number = 1;
// simple, number is now 1 instead of 0

int number2 = 40;

number2 = number;
// Number2 is now 1

