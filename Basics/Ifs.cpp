// If statements are the basics of getting different things to happen
// in different conditions

bool flag = false;

// Booleans can just be used without needing an operator
if(flag){
	// Code a
	// Wont Run
}
else {
	// Code b
	// Will run
}

// or

int i = 3;

if (i == 1){
	// Code a
}
else if(i == 2){
	// Code b
}
else if(i == 3){
	// Code c
	// Will run
}
else {
  // Code d
}

// There are many different operators you can use with if statements

// ==
// if left is equal to right

// !=
// If left is not equal to right

// >
// If left is greater than right

// <
// If left is less than right

// <=
// If left is less than or equal to right
